#include "ft_printf.h"
#include <stdlib.h>

int		ft_clearlist(t_pf **l)
{
	if (l && *l)
	{
		(*l)->p = 0;
		(*l)->m = 0;
		(*l)->o = 0;
		(*l)->s = 0;
		(*l)->nol = 0;
		(*l)->i1 = 0;
		(*l)->i1was = 0;
		(*l)->point = 0;
		(*l)->i2 = 0;
		(*l)->i2was = 0;
		(*l)->f = 0;
		(*l)->c = 0;
		(*l)->all = 0;
		free(*l);
		*l = NULL;
	}
	return (1);
}
