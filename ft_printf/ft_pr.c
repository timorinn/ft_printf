#include <stdio.h>

#include "ft_printf.h"

int		ft_pr(t_pf *l, char **ms)
{
	l->i1 = (l->i1 <= 0 ? 1 : l->i1);
	if (((l->m) && (*ms = ft_stradd(ms, '%', 1)) &&
	(*ms = ft_stradd(ms, ' ', l->i1 - 1))) ||
	(!(l->m) && ((*ms = ft_stradd(ms, ' ', l->i1 - 1)) &&
	(*ms = ft_stradd(ms, '%', 1)))))
		return (1);
	return (0);
}